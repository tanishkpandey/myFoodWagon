  const [signupFormField, setSignupFormField] = useState({
    name: "",
    email: "",
    password: "",
  });
  const handleInputChange = (e) => {
    setSignupFormField({
      ...signupFormField,
      [e.target.name]: e.target.value,
    });
  };
